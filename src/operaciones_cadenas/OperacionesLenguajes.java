    private void btnUnionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUnionActionPerformed
        String l1 = txtL1.getText().trim();
    String l2 = txtL2.getText().trim();
    
    Set<Character> union = new LinkedHashSet<>();
    for (char c : l1.toCharArray()) union.add(c);
    for (char c : l2.toCharArray()) union.add(c);

    StringBuilder resultado = new StringBuilder();
    for (char c : union) resultado.append(c);

    txtResultado.setText(resultado.toString().isEmpty() ? "ε" : resultado.toString());
    }//GEN-LAST:event_btnUnionActionPerformed

    private void btnInterseccionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInterseccionActionPerformed
        String l1 = txtL1.getText().trim();
    String l2 = txtL2.getText().trim();

    Set<Character> set1 = new HashSet<>();
    for (char c : l1.toCharArray()) set1.add(c);

    Set<Character> interseccion = new LinkedHashSet<>();
    for (char c : l2.toCharArray()) {
        if (set1.contains(c)) interseccion.add(c);
    }

    StringBuilder resultado = new StringBuilder();
    for (char c : interseccion) resultado.append(c);

    txtResultado.setText(resultado.toString().isEmpty() ? "ε" : resultado.toString());
    }//GEN-LAST:event_btnInterseccionActionPerformed


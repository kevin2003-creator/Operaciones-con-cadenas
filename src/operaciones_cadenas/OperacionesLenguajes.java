    private void btncomplementoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncomplementoActionPerformed
     // Obtener los textos de los campos
    String universoStr = txtuniverso.getText().trim();
    String l1Str = txtL1.getText().trim();
    String l2Str = txtL2.getText().trim();
    
    // Verificar que el universo no esté vacío
    if (universoStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Error: Debe ingresar el universo");
        return;
    }
    
    // Separar los elementos (asumiendo que están separados por comas)
    Set<String> universo = new HashSet<>(Arrays.asList(universoStr.split("\\s*,\\s*")));
    Set<String> lenguaje = new HashSet<>();
    
    // Agregar elementos de L1 y L2 al lenguaje
    if (!l1Str.isEmpty()) {
        lenguaje.addAll(Arrays.asList(l1Str.split("\\s*,\\s*")));
    }
    if (!l2Str.isEmpty()) {
        lenguaje.addAll(Arrays.asList(l2Str.split("\\s*,\\s*")));
    }
    
    // Validar que el lenguaje esté contenido en el universo
    Set<String> fueraDelUniverso = new HashSet<>(lenguaje);
    fueraDelUniverso.removeAll(universo);
    
    if (!fueraDelUniverso.isEmpty()) {
        JOptionPane.showMessageDialog(this,
            "Error: Las siguientes palabras no están en el universo:\n" + 
            String.join(", ", fueraDelUniverso));
        return;
    }
    
    // Calcular el complemento
    Set<String> complemento = new HashSet<>(universo);
    complemento.removeAll(lenguaje);
    
    // Mostrar el resultado
    if (complemento.isEmpty()) {
        txtResultado.setText("ε");
    } else {
        txtResultado.setText(String.join(", ", complemento));
    }
    }//GEN-LAST:event_btncomplementoActionPerformed


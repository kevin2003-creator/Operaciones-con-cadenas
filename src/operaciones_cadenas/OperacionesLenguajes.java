    private void btnConcatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConcatActionPerformed
        String[] l1 = txtL1.getText().split(",");
        String[] l2 = txtL2.getText().split(",");
        Set<String> concatenacion = new HashSet<>();
        for (String s1 : l1) {
            for (String s2 : l2) {
                concatenacion.add(s1.trim() + s2.trim());
            }
        }
        txtResultado.setText("Concatenación:\n" +  concatenacion);
    }//GEN-LAST:event_btnConcatActionPerformed

    private void btnPotenciaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPotenciaActionPerformed
       String texto = txtL1.getText();
        String potenciaStr = txtPotencia.getText();

        try {
            int potencia = Integer.parseInt(potenciaStr);

            if (potencia < 0) {
                txtResultado.setText("La potencia debe ser un número positivo.");
                return;
            }

            String resultado = texto.repeat(potencia);
            txtResultado.setText("["+ resultado + "]");

        } catch (NumberFormatException ex) {
            txtResultado.setText("Ingrese un número válido en el campo de potencia.");
        }
    }//GEN-LAST:event_btnPotenciaActionPerformed


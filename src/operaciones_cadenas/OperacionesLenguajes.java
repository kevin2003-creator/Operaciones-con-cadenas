private void copiarAlPortapapeles(String texto) {
    StringSelection seleccion = new StringSelection(texto);
    Clipboard portapapeles = Toolkit.getDefaultToolkit().getSystemClipboard();
    portapapeles.setContents(seleccion, null);
    JOptionPane.showMessageDialog(this, "Resultado copiado al portapapeles");
}
// Método para limpiar el texto del usuario y convertirlo en un conjunto de palabras válidas
private Set<String> limpiarYSeparar(String texto) {
    return Arrays.stream(texto.split(","))
            .map(s -> s.replaceAll("\\s+", "")) // elimina todos los espacios, tabs, saltos de línea
            .filter(s -> !s.isEmpty())          // descarta cadenas vacías
            .collect(Collectors.toSet());
}

    

    
